<pre>
    ---------------------------------------------------------------------------------------------------------------------------------------------------------------------
#    Selector                                                #                     Description                                                                     #
---------------------------------------------------------------------------------------------------------------------------------------------------------------------
1. Universal Selector: *                                           Selects all elements in the document.
2. Type Selector: element                                           Selects all elements of the specified type.
3. Class Selector: .class                                           Selects all elements with the specified class.
4. ID Selector: #id                                                 Selects the element with the specified ID.
5. Descendant Selector: ancestor descendant          Selects all descendants of the specified ancestor element.
6. Child Selector: parent > child                                  Selects all direct children of the specified parent element.
7. Adjacent Sibling Selector: element + adjacent         Selects the element that is immediately preceded by the specified adjacent element.
8. General Sibling Selector: element ~ sibling            Selects all siblings that follow the specified element.
9. Attribute Selector: [attribute], [attribute=value], [attribute~=value], [attribute|=value], [attribute^=value], [attribute$=value], [attribute*=value]           Selects elements based on the presence or value of the specified attribute.
10. Pseudo-classes: :hover, :active, :focus, :first-child, :last-child, :nth-child(), :nth-last-child(), :nth-of-type(), :nth-last-of-type(), :not(), :empty, :target, :checked, :enabled, :disabled, :required, :valid, :invalid                      Selects elements based on their state or position in the document.
11. Pseudo-elements: ::before, ::after, ::first-line, ::first-letter, ::selection                           Selects and styles parts of an element's content.
12. Grouping Selector: selector1, selector2, selector3      Selects all elements that match any of the specified selectors.
---------------------------------------------------------------------------------------------------------------------------------------------------------------------
